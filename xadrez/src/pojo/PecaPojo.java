package pojo;

public class PecaPojo {
	private int linha;
	private int coluna;
	private char tipoPecao;
	private char cor;
	
	public int getLinha() {
		return linha;
	}
	public void setLinha(int linha) {
		this.linha = linha;
	}
	public int getColuna() {
		return coluna;
	}
	public void setColuna(int coluna) {
		this.coluna = coluna;
	}
	public char getTipoPecao() {
		return tipoPecao;
	}
	public void setTipoPecao(char tipoPecao) {
		this.tipoPecao = tipoPecao;
	}
	public char getCor() {
		return cor;
	}
	public void setCor(char cor) {
		this.cor = cor;
	}
}
